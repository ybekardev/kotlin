package org.kotlin.warmup.bradtraversy

    fun main(){
        println("Hello World!")

        //var can have a reassigned value
        var x: Int = 34
        println(x)
        x = 53
        println(x)

        //val can NOT have a reassigned value
        val y: Int = 5
        //y = 6; can't reassign a value

        //value does not have to specify it is an Int
        val a = 21

        //Long
        val b = 23L

        //Double
        val c: Double = 3.554

        //float
        val d = 546f

        //bignumber
        val f = 4321L

        //boolean
        val iLoveVegetable = true
    }



